package model;

public class Vehicle {

    private String name;
    private int charge;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setCharge(int charge) {
        this.charge = charge;
    }
    public int getCharge() {
        return charge;
    }
}
